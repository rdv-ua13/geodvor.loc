					Подходит для применения в сфере строительства, но основная область применения — обустройство территории и ландшафтный дизайн. Материал применяют для организации садовых дорожек, невысоких насыпей, клумб, газонов.
					<br>
					<br>
					<div class="gap-xxxs">
						<div class="fw-semibold fz-subheading-sm">150-200 г/м2</div>
					</div>
					Полотно применяют для разделения и изоляции слоев, укрепления пирога пешеходных дорожек, а также для повышения стабильности и усиления несущей способности оснований под фундаментами.
					<br>
					<br>
					<div class="gap-xxxs">
						<div class="fw-semibold fz-subheading-sm">250 г/м2</div>
					</div>
					Основное назначение — строительство автомобильных трасс и парковок. Служит разделителем сыпучих материалов различных фракций и обеспечивает их экономию. Кроме того, геотекстиль данной плотности увеличивает срок службы дорожного полотна.
					<br>
					<br>
					<div class="gap-xxxs">
						<div class="fw-semibold fz-subheading-sm">300-350 г/м2</div>
					</div>
					Предназначен для усиления слабых оснований автомобильный дорог с интенсивным трафиком, организации парковок грузовых автомобилей, площадок логистических центров, а также при возведении насыпей значительной высоты.
					<br>
					<br>
					<div class="gap-xxxs">
						<div class="fw-semibold fz-subheading-sm">400 г/м2</div>
					</div>
					Используется для создания инфраструктуры аэропортов (взлетно-посадочные полосы, рулежные дорожки, стоянки самолетов), а также для строительства автомобильных магистралей с интенсивным движением грузового автотранспорта.
					<br>
					<br>
					<div class="gap-xxxs">
						<div class="fw-semibold fz-subheading-sm">450 г/м2</div>
					</div>
					Применяется в процессе строительства фундаментов высотных зданий, так как обладает значительной армирующей способностью. Также часто используется для обустройства оснований менее массивных конструкций на слабых грунтах.
